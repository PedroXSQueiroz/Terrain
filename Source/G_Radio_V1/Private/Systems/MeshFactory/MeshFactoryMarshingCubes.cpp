		FVector secondVertex = this->InteroplateEdge(
			cornerOffsets[secondEdgeVertex0]
			, this->GetDensity(secondEdgeVertex0Index, densities)
			, cornerOffsets[secondEdgeVertex1]
			, this->GetDensity(secondEdgeVertex1Index, densities)
		);



