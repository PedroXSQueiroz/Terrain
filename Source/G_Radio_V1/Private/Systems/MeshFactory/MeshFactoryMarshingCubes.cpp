	int cubeIndex = 0;

	if (this->GetDensity(cubeDensitiesIndexes[0], densities) < this->Config->IsoLevel) cubeIndex |= 1;
	if (this->GetDensity(cubeDensitiesIndexes[1], densities) < this->Config->IsoLevel) cubeIndex |= 2;
	if (this->GetDensity(cubeDensitiesIndexes[2], densities) < this->Config->IsoLevel) cubeIndex |= 4;
	if (this->GetDensity(cubeDensitiesIndexes[3], densities) < this->Config->IsoLevel) cubeIndex |= 8;
	if (this->GetDensity(cubeDensitiesIndexes[4], densities) < this->Config->IsoLevel) cubeIndex |= 16;
